    private void update_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_btActionPerformed
        try{
            getDataFromFrame();
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Enter a Valid Birth Date or Mobile number."+e,"Error",JOptionPane.INFORMATION_MESSAGE);return;
        }

        //Fileds Validation
        if(fname.equals("")){
            JOptionPane.showMessageDialog(null, "Enter First Name.!!","Enter full Details",JOptionPane.INFORMATION_MESSAGE);return;}
        if(lname.equals("")){
            JOptionPane.showMessageDialog(null, "Enter last Name.!!","Enter full Details",JOptionPane.INFORMATION_MESSAGE);return;}
        if(user.equals("")){
            JOptionPane.showMessageDialog(null, "Enter Username Name.!!","Enter full Details",JOptionPane.INFORMATION_MESSAGE);return;}
        if(pass.equals("")){
            JOptionPane.showMessageDialog(null, "Enter Password.!!","Enter full Details",JOptionPane.INFORMATION_MESSAGE);return;}

        pf.Query="Update register set fname='"+fname+"', lname='"+lname+"', dob="+dob+", gender='"+gender+"', mobile="+mobile+", email='"+email+"', address='"+add+"', pass='"+pass+"', UserType='"+utype+"' where username='"+user+"';";
        pf.updtrec();
        JOptionPane.showMessageDialog(this, "Record Update Sucessfully");
        emptyTable();
        getRecord();
    }//GEN-LAST:event_update_btActionPerformed

