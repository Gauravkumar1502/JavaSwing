    void setRecord()
    {
        add_bt.setEnabled(false);
        username_tf.setEditable(false);
        update_bt.setEnabled(true);
        delete_bt.setEnabled(true);
        clear_bt.setEnabled(true);
        DefaultTableModel model= (DefaultTableModel) showrecords_tb.getModel();
        int row=showrecords_tb.getSelectedRow();
        fname_tf.setText(model.getValueAt(row, 0).toString());
        lname_tf.setText(model.getValueAt(row, 1).toString());
        username_tf.setText(model.getValueAt(row, 2).toString());
        dob_tf.setText(model.getValueAt(row, 3).toString().replace("-", ""));
        String gen=(model.getValueAt(row, 4).toString());
        if(gen.equals("M")) male_rb.setSelected(true);
        if(gen.equals("F")) female_rb.setSelected(true);
        mobile_tf.setText(model.getValueAt(row, 5).toString());
        email_tf.setText(model.getValueAt(row, 6).toString());
        address_tf.setText(model.getValueAt(row, 7).toString());
        pass_tf.setText(model.getValueAt(row, 8).toString());
        String usertype=(model.getValueAt(row, 9).toString());
        if(usertype.equals("Admin")) adm_rb.setSelected(true);
        if(usertype.equals("Teacher")) teacher_rb.setSelected(true);
        if(usertype.equals("Student")) student_rb.setSelected(true);
    }
